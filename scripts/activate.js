require("dotenv").config()
const API_URL = process.env.ALCHEMY_URL
const PUBLIC_KEY = process.env.BENEFICIARY_ADDRESS
const PRIVATE_KEY = process.env.BENEFICIARY_KEY

const { createAlchemyWeb3 } = require("@alch/alchemy-web3")
const web3 = createAlchemyWeb3(API_URL)

const contract = require("../artifacts/contracts/token.sol/Token.json")
const contractAddress = "0xe28adBBDC6a866517D6E9796378D3EF51f32c1Ee"
const nftContract = new web3.eth.Contract(contract.abi, contractAddress)

async function activate() {
  const nonce = await web3.eth.getTransactionCount(PUBLIC_KEY, "latest") //get latest nonce

  //the transaction
  const tx = {
    from: PUBLIC_KEY,
    to: contractAddress,
    nonce: nonce,
    gas: 500000,
    data: nftContract.methods.setTransfersAllowed(true).encodeABI(),
  }

  const signPromise = web3.eth.accounts.signTransaction(tx, PRIVATE_KEY)
  signPromise
    .then((signedTx) => {
      web3.eth.sendSignedTransaction(
        signedTx.rawTransaction,
        function (err, hash) {
          if (!err) {
            console.log(
              "The hash of your transaction is: ",
              hash
            )
          } else {
            console.log(
              "Something went wrong when submitting your transaction:",
              err
            )
          }
        }
      )
    })
    .catch((err) => {
      console.log(" Promise failed:", err)
    })
}

activate()
